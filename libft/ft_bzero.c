#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*d;

	if (n == 0)
		return ;
	d = (unsigned char *)s;
	while (n > 0)
	{
		*d++ = 0;
		n--;
	}
}
