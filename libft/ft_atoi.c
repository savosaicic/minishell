#include "libft.h"

int	ft_atoi(const char *str)
{
	long	res;
	long	negative;

	negative = 1;
	res = 0;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t'
			|| *str == '\v' || *str == '\f' || *str == '\r'))
		++str;
	if (*str == '-')
		negative = -1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str && *str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - 48);
		++str;
	}
	// if (res * negative > 2147483647 || res * negative < -2147483648)
	// 	return (0);
	return (res * negative);
}
